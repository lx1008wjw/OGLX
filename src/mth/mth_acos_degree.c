/*+ FUNCTION DESCRIPTION ----------------------------------------------
  NAME: mth_acos
  DESCRIPTION:
    Function shall compute the arc cosine of given value.
  PARAMETERS:
    par_f_t -> cosine (in range [-1.0, 1.0])
  RETURN:
    Arc cosine of given value in radians (precision 0.02 radians max)
---------------------------------------------------------------------+*/
SGLfloat mth_acos(SGLfloat par_f_t)
{
    SGLfloat loc_f_return;
    SGLfloat loc_f_t = par_f_t;

    /* Limit angle between [-1.0,1.0] for precision issues */
    if (loc_f_t < -1.0F) {
        loc_f_t = -1.0F;
    }

    if (loc_f_t > 1.0F) {
        loc_f_t = 1.0F;
    }

    if (loc_f_t >= 0.98F) {
        if (sgluFloatIsEqual(loc_f_t, 1.0F)) {
            loc_f_return = 0.0F;
        }
        else {
            SGLlong loc_l_index = MTH_ACOS_SIZE - SGLfloat_to_int32((1.0F - loc_f_t) * 10000.0F);
            loc_f_return = mth_table_acos_plus[sgluGetTableIndex(loc_l_index, MTH_ACOS_SIZE)];
        }
    }
    else {
        if (loc_f_t <= -0.98F) {
            if (sgluFloatIsEqual(loc_f_t, -1.0F)) {
                loc_f_return = MTH_PI;
            }
            else {
                SGLlong loc_l_index = MTH_ACOS_SIZE - SGLfloat_to_int32((1.0F - (-loc_f_t)) * 10000.0F);
                loc_f_return = mth_table_acos_minus[sgluGetTableIndex(loc_l_index, MTH_ACOS_SIZE)];
            }
        }
        else {
            SGLfloat loc_f_a = (SGLfloat) (-0.939115566365855);
            SGLfloat loc_f_b = (SGLfloat) 0.9217841528914573;
            SGLfloat loc_f_c = (SGLfloat) (-1.2845906244690837);
            SGLfloat loc_f_d = (SGLfloat) 0.295624144969963174;

            SGLfloat loc_f_x = loc_f_t;
            SGLfloat loc_f_x2 = loc_f_t * loc_f_t;
            SGLfloat loc_f_x3 = loc_f_t * loc_f_t * loc_f_t;
            SGLfloat loc_f_x4 = loc_f_t * loc_f_t * loc_f_t * loc_f_t;

            loc_f_return = (MTH_PI / 2.0F) + SGLfloat_div((loc_f_a * loc_f_x) + (loc_f_b * loc_f_x3), 1.0F + (loc_f_c * loc_f_x2) + (loc_f_d * loc_f_x4));
        }
    }
    return loc_f_return;
}

